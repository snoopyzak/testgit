int	ft_is_prime(int nb)
{
	int	i;
	int	c;

	if (nb < 2)
		return (0);
	i = 1;
	c = 0;
	while (i <= nb)
	{
		if (nb % i == 0)
			c++;
		i++;
	}
	if (c == 2)
		return (1);
	else
		return (0);
}
